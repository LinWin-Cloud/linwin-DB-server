package remote;

public class Users {
    private String key;
    private String MirrorName;

    public void setKey(String key) {
        this.key = key;
    }
    public void setMirrorName(String mirrorName) {
        this.MirrorName = mirrorName;
    }
    public String getKey() {
        return this.key;
    }
    public String getMirrorName() {
        return this.MirrorName;
    }
}
